int		tgrid_validate_pieces(t_tgrid *tg)
{
	t_tpc *i;
	t_tpc *j;

	i = tg->pieces;
	while (i - tg->pieces < tg->n)
	{
		j = g_valid_pieces;
		while (j - g_valid_pieces < 19 && j->as_ull != i->as_ull)
			j++;
		if (j - g_valid_pieces == 19)
			return (0);
		i++;
	}
	return (1);
}
